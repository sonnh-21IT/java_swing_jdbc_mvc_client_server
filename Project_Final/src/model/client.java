package model;

import java.net.InetAddress;
import java.net.Socket;

public class client {
	private String host;
	private int port;
	
	public client(String host, int port) {
		this.host = host;
		this.port = port;
	}

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}
	
}
